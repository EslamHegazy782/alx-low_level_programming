#include "3-calc.h"

/**
 *op_add - adds 2 integers
 *@a: 1st integer
 *@b: 2nd integer
 *Return: sum
*/

int op_add(int a, ant b)
{
	return (a + b);
}

/**
 *op_sub - subtracts 2 no.
 *@a: 1st int
 *@b: 2nd int
 *Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - multiply 2 no.
 *@a: 1st int
 *@b: 2nd int
 *Return: multiply of 2 no.
 */
int op_mul(int a, int b)
{
	return (a * b );
}

/**
 *op_div - divide 2 int
 *@a: 1st int
 *@b: 2nd int
 *Return: quotient
 *
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - mod of 2 ints
 *@a: 1st int
 *@b: 2nd int
 *Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
